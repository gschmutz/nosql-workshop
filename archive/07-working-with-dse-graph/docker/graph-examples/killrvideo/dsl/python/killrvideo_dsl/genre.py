from aenum import Enum


class Genre(Enum):
    """The available genres of movies in the KillrVideo dataset"""

    ACTION = "Action"
    ADVENTURE = "Adventure"
    ANIMATION = "Animation"
    COMEDY = "Comedy"
    DOCUMENTARY = "Documentary"
    DRAMA = "Drama"
    FANTASY = "Fantasy"
    FILM_NOIR = "Film-Noir"
    HORROR = "Horror"
    KIDS = "Kids"
    MUSICAL = "Musical"
    MYSTERY = "Mystery"
    ROMANCE = "Romance"
    SCI_FI = "Sci-Fi"
    TV_SERIES = "TV Series"
    THRILLER = "Thriller"
    WAR = "War"
    WESTERN = "Western"
